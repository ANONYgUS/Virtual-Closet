// stores the temperature of the factor of clothing

public class Temperature{

  private double impact;
  private int approx;
  private int range;

  /**
  * constructor for temperature objects
  */
  public Temperature(){

  }

  /**
  * returns the impact of the temperature
  * @return impact
  */
  public double getImpact(){
    return impact;
  }

  /**
  * returns the approximate temperature of the factor of clothing
  * @return approximate temperature
  */
  public int getApprox(){
    return approx;
  }

  /**
  * returns the radius of the range of acceptable temperatures of the factor of clothing
  * @return radius of range of acceptable temperatures
  */
  public int getApprox(){
    return range;
  }
}
